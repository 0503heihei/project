        <li>
            <p>IDATE数据显示，截至2014年底，拉美市场FTTH用户达到266万，相比2013年底时的数据增长了57%；同时FTTH家庭覆盖数达到1500万，年增幅达到45.6%。</p>
            <p>IDATE预测，到2019年底，拉美地区的FTTH用户将增长至1050万，而FTTH家庭覆盖数则将上升至5600万。</p>
            <p>从使用率（FTTH用户/FTTH家庭覆盖数）的角度来看，从2014年到2019年，该比率从17.7%上升至18.8%，上升幅度不多。现阶段，在大多数拉美国家，FTTH覆盖的家庭占总家庭数的7%到10%，而使用率则大多在1%到3%之间。2014年17.7%的使用率是被少数发展快速的国家推高所致。比如，乌拉圭和巴巴多斯的FTTH家庭覆盖率分别达到95%和87%，其中乌拉圭的FTTH使用率达到43%。</p>
            <p>IDATE的FTTH数据包含了FTTB数据，不过FTTB用户仅占其中的9%，不同于FTTB占比较高的欧洲FTTH市场。</p>
